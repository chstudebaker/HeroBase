package com.hbapi;

public class Powers{
	private int powerID;
	private String description;
	private int heroID;
	private String explanation;

	public int getPowerID(){
		return powerID;
	}

	public String getDescription(){
		return description;
	}

	public int getHeroID(){
		return heroID;
	}

	public String getExplanation(){
		return explanation;
	}
}
